package src;

class Position
{
    int x_axis;
    int y_axis;

    public Position(int x_axis, int y_axis)
    {
        this.x_axis = x_axis;
        this.y_axis = y_axis;
    }
}

class Velocity
{
    int x_axis;
    int y_axis;

    public Velocity(int x_axis, int y_axis)
    {
        this.x_axis = x_axis;
        this.y_axis = y_axis;
    }
}